package Constr;

public class SalesReport {
	String year;
	String month;
	String sumsale;
	
	public SalesReport(String year,String month,String sumsale) {
		this.year = year;
		this.month = month;
		this.sumsale = sumsale;
	}
	public String getyear() {
		return year;
	}
	public String getmonth() {
		return month;
	}
	public String getsumsale() {
		return sumsale;
	}




	
	

	
	
}
